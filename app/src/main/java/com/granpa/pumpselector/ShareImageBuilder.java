    public static Bitmap build(Activity a, PumpRecord r, boolean has, double head, double flow) {
        return build(a, r, has, head, flow, "LPH");
    }

    static void box(Canvas c, float l, float t, float r, float b, int fill, int stroke) {
        Paint p = new Paint(1);
        p.setStyle(Paint.Style.FILL);
        p.setColor(fill);
        c.drawRoundRect(new RectF(l, t, r, b), 18, 18, p);
        p.setStyle(Paint.Style.STROKE);
        p.setStrokeWidth(2);
        p.setColor(stroke);
        c.drawRoundRect(new RectF(l, t, r, b), 18, 18, p);
    }

    static void drawChip(Canvas c, String top, String bottom, int x, int y, int w, int h) {
        box(c, x, y, x + w, y + h, Color.WHITE, Color.rgb(207, 221, 236));

        Paint t = new Paint(1);
        t.setTextAlign(Paint.Align.CENTER);
        t.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        t.setTextSize(24);
        t.setColor(Color.rgb(0, 83, 176));
        c.drawText(top, x + w / 2f, y + 40, t);

        t.setColor(Ui.TEXT);
        t.setTextSize(bottom.contains("\n") ? 23 : 27);
        String[] lines = bottom.split("\n");
        float sy = lines.length == 1 ? y + 82 : y + 72;
        for (int i = 0; i < lines.length && i < 2; i++) {
            c.drawText(lines[i], x + w / 2f, sy + i * 28, t);
        }
    }

    static void drawFit(Canvas c, String s, float x, float y, Paint p, float max) {
        float old = p.getTextSize();
        while (p.measureText(s) > max && p.getTextSize() > 22) p.setTextSize(p.getTextSize() - 1);
        c.drawText(s, x, y, p);
        p.setTextSize(old);
    }

    static void drawChart(Canvas c, PumpRecord r, boolean has, double head, double flow, RectF outer, String unit) {
        unit = PumpSelector.normalizeUnit(unit);

        Paint p = new Paint(1);
        p.setStyle(Paint.Style.FILL);
        p.setColor(Color.WHITE);
        c.drawRoundRect(outer, 20, 20, p);
        p.setStyle(Paint.Style.STROKE);
        p.setStrokeWidth(2);
        p.setColor(Ui.BORDER);
        c.drawRoundRect(outer, 20, 20, p);

        Paint title = new Paint(1);
        title.setColor(Ui.TEXT);
        title.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        title.setTextSize(30);
        c.drawText("Performance Curve", outer.left + 32, outer.top + 42, title);

        RectF plot = new RectF(outer.left + 95, outer.top + 82, outer.right - 35, outer.bottom - 160);

        ArrayList<double[]> real = new ArrayList<>();
        if (r.curve != null) {
            for (double[] q : r.curve) {
                if (q != null && q.length >= 2) real.add(new double[]{q[0], PumpSelector.fromLPH(q[1], unit)});
            }
        }
        Collections.sort(real, Comparator.comparingDouble(a -> a[1]));
        if (real.isEmpty()) return;

        ArrayList<double[]> pts = displayPoints(real, has ? head : null, has ? PumpSelector.fromLPH(flow, unit) : null);

        double maxF = 0, maxH = 0;
        for (double[] q : pts) {
            maxF = Math.max(maxF, q[1]);
            maxH = Math.max(maxH, q[0]);
        }
        double axisF = roundUp(maxF * 1.04, niceFlowStep(maxF, unit));
        double axisH = roundUp(maxH * 1.08, 10);

        Paint grid = new Paint(1);
        grid.setColor(Color.rgb(194, 208, 224));
        grid.setStrokeWidth(2.0f);
        grid.setStyle(Paint.Style.STROKE);
        grid.setPathEffect(new DashPathEffect(new float[]{8, 8}, 0));

        Paint minorGrid = new Paint(1);
        minorGrid.setColor(Color.rgb(218, 228, 239));
        minorGrid.setStrokeWidth(1.2f);
        minorGrid.setStyle(Paint.Style.STROKE);

        Paint text = new Paint(1);
        text.setColor(Ui.TEXT);
        text.setTextSize(22);
        text.setTextAlign(Paint.Align.CENTER);

        for (int i = 1; i < 20; i++) {
            if (i % 4 == 0) continue;
            float gx = plot.left + i * plot.width() / 20f;
            c.drawLine(gx, plot.top, gx, plot.bottom, minorGrid);
        }
        for (int i = 1; i < 20; i++) {
            if (i % 4 == 0) continue;
            float gy = plot.bottom - i * plot.height() / 20f;
            c.drawLine(plot.left, gy, plot.right, gy, minorGrid);
        }

        for (int i = 0; i <= 5; i++) {
            float gx = plot.left + i * plot.width() / 5f;
            c.drawLine(gx, plot.top, gx, plot.bottom, grid);
            c.drawText(PumpSelector.formatFlowNumber(axisF * i / 5, unit), gx, plot.bottom + 34, text);
        }

        text.setTextAlign(Paint.Align.RIGHT);
        for (int i = 0; i <= 5; i++) {
            float gy = plot.bottom - i * plot.height() / 5f;
            c.drawLine(plot.left, gy, plot.right, gy, grid);
            c.drawText(String.format(Locale.US, "%.0f", axisH * i / 5), plot.left - 14, gy + 8, text);
        }

        Paint axis = new Paint(1);
        axis.setColor(Ui.MUTED);
        axis.setStrokeWidth(2);
        c.drawLine(plot.left, plot.top, plot.left, plot.bottom, axis);
        c.drawLine(plot.left, plot.bottom, plot.right, plot.bottom, axis);

        text.setTextAlign(Paint.Align.CENTER);
        text.setTextSize(26);
        c.drawText("Flow Rate (" + PumpSelector.unitLabel(unit) + ")", plot.centerX(), plot.bottom + 74, text);
        c.save();
        c.rotate(-90, outer.left + 42, plot.centerY());
        c.drawText("Head (m)", outer.left + 42, plot.centerY(), text);
        c.restore();

        Paint cp = new Paint(1);
        cp.setColor(Color.rgb(0, 96, 216));
        cp.setStyle(Paint.Style.STROKE);
        cp.setStrokeWidth(5);
        cp.setStrokeCap(Paint.Cap.ROUND);
        cp.setStrokeJoin(Paint.Join.ROUND);

        Path path = new Path();
        for (int i = 0; i < pts.size(); i++) {
            float cx = x(pts.get(i)[1], axisF, plot), cy = y(pts.get(i)[0], axisH, plot);
            if (i == 0) path.moveTo(cx, cy);
            else {
                float px = x(pts.get(i - 1)[1], axisF, plot), py = y(pts.get(i - 1)[0], axisH, plot);
                float mx = (px + cx) / 2f;
                path.cubicTo(mx, py, mx, cy, cx, cy);
            }
        }
        c.drawPath(path, cp);

        if (has) {
            double flowDisplay = PumpSelector.fromLPH(flow, unit);
            float sx = x(flowDisplay, axisF, plot), sy = y(head, axisH, plot);

            Paint dash = new Paint(1);
            dash.setColor(Color.rgb(255, 132, 0));
            dash.setStyle(Paint.Style.STROKE);
            dash.setStrokeWidth(2.4f);
            dash.setPathEffect(new DashPathEffect(new float[]{12, 10}, 0));
            c.drawLine(plot.left, sy, sx, sy, dash);
            c.drawLine(sx, sy, sx, plot.bottom, dash);

            drawHeadBadge(c, formatHead(head), plot.left, sy);
            drawFlowBadge(c, PumpSelector.formatFlowNumber(flowDisplay, unit), sx, plot.bottom + 34, plot);

            Paint glow = new Paint(1);
            glow.setStyle(Paint.Style.FILL);
            glow.setColor(Color.argb(45, 255, 132, 0));
            c.drawCircle(sx, sy, 30, glow);

            Paint halo = new Paint(1);
            halo.setColor(Color.WHITE);
            halo.setStyle(Paint.Style.FILL);
            c.drawCircle(sx, sy, 19, halo);

            Paint dot = new Paint(1);
            dot.setColor(Color.rgb(255, 132, 0));
            dot.setStyle(Paint.Style.FILL);
            c.drawCircle(sx, sy, 14, dot);
        }

        Paint leg = new Paint(1);
        leg.setTextSize(23);
        leg.setColor(Ui.TEXT);
        c.drawText("Selected operating point", outer.left + 115, outer.bottom - 42, leg);
    }

    static void drawHeadBadge(Canvas c, String label, float axisX, float centerY) {
        Paint bg = new Paint(1);
        bg.setColor(Color.rgb(255, 132, 0));
        bg.setStyle(Paint.Style.FILL);

        Paint t = new Paint(1);
        t.setColor(Color.WHITE);
        t.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        t.setTextSize(22);

        float pad = 14;
        float bw = t.measureText(label) + pad * 2;
        RectF r = new RectF(axisX - bw - 12, centerY - 22, axisX - 12, centerY + 22);
        c.drawRoundRect(r, 9, 9, bg);
        c.drawText(label, r.centerX() - t.measureText(label) / 2, r.centerY() + 8, t);
    }

    static void drawFlowBadge(Canvas c, String label, float centerX, float centerY, RectF plot) {
        Paint bg = new Paint(1);
        bg.setColor(Color.rgb(255, 132, 0));
        bg.setStyle(Paint.Style.FILL);

        Paint t = new Paint(1);
        t.setColor(Color.WHITE);
        t.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        t.setTextSize(22);

        float pad = 16;
        float bw = Math.min(t.measureText(label) + pad * 2, 155);
        RectF r = new RectF(centerX - bw / 2, centerY - 22, centerX + bw / 2, centerY + 22);
        if (r.left < plot.left) r.offset(plot.left - r.left, 0);
        if (r.right > plot.right) r.offset(plot.right - r.right, 0);
        c.drawRoundRect(r, 9, 9, bg);
        drawCenteredFit(c, label, r, t);
    }

    static void drawCenteredFit(Canvas c, String label, RectF r, Paint p) {
        float old = p.getTextSize();
        while (p.measureText(label) > r.width() - 12 && p.getTextSize() > 15) p.setTextSize(p.getTextSize() - 1);
        c.drawText(label, r.centerX() - p.measureText(label) / 2, r.centerY() + 8, p);
        p.setTextSize(old);
    }

    static String formatHead(double v) {
        return Math.abs(v - Math.round(v)) < 0.05 ? String.format(Locale.US, "%.0f", v) : String.format(Locale.US, "%.1f", v);
    }

    static ArrayList<double[]> displayPoints(ArrayList<double[]> real, Double selH, Double selFDisplay) {
        ArrayList<double[]> pts = new ArrayList<>();
        for (double[] p : real) pts.add(new double[]{p[0], p[1]});
        if (selH != null && selFDisplay != null && !Double.isNaN(selH) && !Double.isNaN(selFDisplay)) {
            boolean duplicate = false;
            for (double[] p : pts) if (Math.abs(p[0] - selH) < 0.01 && Math.abs(p[1] - selFDisplay) < 0.01) duplicate = true;
            if (!duplicate) pts.add(new double[]{selH, selFDisplay});
        }
        Collections.sort(pts, Comparator.comparingDouble(a -> a[1]));
        return pts;
    }

    public static void shareWhatsApp(Activity a, PumpRecord r, boolean has, double head, double flow, String unit) {
        share(a, r, has, head, flow, true, unit);
    }

    public static void shareWhatsApp(Activity a, PumpRecord r, boolean has, double head, double flow) {
        shareWhatsApp(a, r, has, head, flow, "LPH");
    }

    public static void share(Activity a, PumpRecord r, boolean has, double head, double flow, boolean whats, String unit) {
        try {
            Bitmap b = build(a, r, has, head, flow, unit);
            File f = saveCache(a, b);
            Uri u = FileProvider.getUriForFile(a, a.getPackageName() + ".fileprovider", f);

            Intent i = new Intent(Intent.ACTION_SEND);
            i.setType("image/png");
            i.putExtra(Intent.EXTRA_STREAM, u);
            i.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);

            if (whats) {
                i.setPackage("com.whatsapp");
                try { a.startActivity(i); return; }
                catch (Exception ignored) {
                    i.setPackage("com.whatsapp.w4b");
                    try { a.startActivity(i); return; }
                    catch (Exception ignored2) { i.setPackage(null); }
                }
            }
            a.startActivity(Intent.createChooser(i, "Share pump image"));
        } catch (Exception e) {
            Toast.makeText(a, "Unable to create share image", Toast.LENGTH_LONG).show();
        }
    }

    public static void share(Activity a, PumpRecord r, boolean has, double head, double flow, boolean whats) {
        share(a, r, has, head, flow, whats, "LPH");
    }

    public static void download(Activity a, PumpRecord r, boolean has, double head, double flow, String unit) {
        try {
            Bitmap b = build(a, r, has, head, flow, unit);
            String name = "Granpa_" + safe(r.model).replaceAll("[^A-Za-z0-9_-]", "_") + ".png";

            if (Build.VERSION.SDK_INT >= 29) {
                ContentValues cv = new ContentValues();
                cv.put(MediaStore.Images.Media.DISPLAY_NAME, name);
                cv.put(MediaStore.Images.Media.MIME_TYPE, "image/png");
                cv.put(MediaStore.Images.Media.RELATIVE_PATH, Environment.DIRECTORY_PICTURES + "/Granpa");

                Uri uri = a.getContentResolver().insert(MediaStore.Images.Media.EXTERNAL_CONTENT_URI, cv);
                OutputStream os = a.getContentResolver().openOutputStream(uri);
                b.compress(Bitmap.CompressFormat.PNG, 100, os);
                os.close();
            } else {
                File dir = a.getExternalFilesDir(Environment.DIRECTORY_PICTURES);
                if (!dir.exists()) dir.mkdirs();

                File f = new File(dir, name);
                FileOutputStream os = new FileOutputStream(f);
                b.compress(Bitmap.CompressFormat.PNG, 100, os);
                os.close();
            }
            Toast.makeText(a, "Image saved", Toast.LENGTH_LONG).show();
        } catch (Exception e) {
            Toast.makeText(a, "Unable to save image", Toast.LENGTH_LONG).show();
        }
    }

    public static void download(Activity a, PumpRecord r, boolean has, double head, double flow) {
        download(a, r, has, head, flow, "LPH");
    }

    static File saveCache(Activity a, Bitmap bm) throws Exception {
        File dir = new File(a.getCacheDir(), "share");
        if (!dir.exists()) dir.mkdirs();

        File f = new File(dir, "granpa_share.png");
        FileOutputStream os = new FileOutputStream(f);
        bm.compress(Bitmap.CompressFormat.PNG, 100, os);
        os.close();
        return f;
    }

    static double niceFlowStep(double m, String unit) {
        unit = PumpSelector.normalizeUnit(unit);
        if (unit.equals("LPM")) {
            if (m <= 20) return 5;
            if (m <= 60) return 10;
            if (m <= 200) return 25;
            if (m <= 600) return 100;
            if (m <= 2000) return 250;
            return 1000;
        }
        if (unit.equals("LPH")) {
            if (m <= 1000) return 200;
            if (m <= 3000) return 500;
            if (m <= 10000) return 1000;
            if (m <= 30000) return 5000;
            return 10000;
        }
        if (unit.equals("LPS")) return m <= 10 ? 1 : 5;
        return m <= 10 ? 1 : 10;
    }

    static double roundUp(double v, double s) { return Math.ceil(v / s) * s; }
    static float x(double f, double max, RectF r) { return (float) (r.left + (f / max) * r.width()); }
    static float y(double h, double max, RectF r) { return (float) (r.bottom - (h / max) * r.height()); }
    static String safe(String s) { return s == null ? "" : s.trim(); }

    static String phaseLabel(String p) {
        p = safe(p).toUpperCase(Locale.US);
        boolean s = p.contains("S");
        boolean t = p.contains("T") || p.contains("3");
        if (s && t) return "S/T";
        if (s) return "Single";
        if (t) return "Three";
        return p.isEmpty() ? "-" : p;
    }

    static String compactType(String c) {
        c = safe(c).toLowerCase(Locale.US);
        if (c.contains("borewell")) return "Borewell\nSubmersible";
        if (c.contains("monoblock")) return "Monoblock";
        if (c.contains("dewatering") || c.contains("sewage")) return "Dewatering";
        return safe(c).replace(" - ", "\n");
    }

    static String flowRange(PumpRecord r, String unit) {
        if (!Double.isNaN(r.minFlowLPH) && !Double.isNaN(r.maxFlowLPH)) {
            return PumpSelector.formatFlowNumber(PumpSelector.fromLPH(r.minFlowLPH, unit), unit) + "–" + PumpSelector.formatFlow(r.maxFlowLPH, unit);
        }
        return "-";
    }
