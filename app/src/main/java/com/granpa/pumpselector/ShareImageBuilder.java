        text.setColor(Ui.MUTED);
        text.setTypeface(Typeface.DEFAULT);
        text.setTextSize(24);
        String left = "Generated by ";
        c.drawText(left, 165, 1423, text);

        float nx = 165 + text.measureText(left);
        text.setColor(Color.rgb(0, 83, 176));
