        p.setStyle(Paint.Style.FILL);
        c.drawRoundRect(new RectF(30, 30, w - 30, h - 30), 32, 32, p);

        Bitmap logo = BitmapFactory.decodeResource(a.getResources(), R.drawable.app_logo);
        if (logo != null) c.drawBitmap(logo, null, new RectF(70, 70, 150, 150), null);

        Paint text = new Paint(1);
        text.setColor(Ui.TEXT);
        text.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        text.setTextSize(56);
        c.drawText("Granpa", 170, 125, text);

        text.setTextSize(52);
        c.drawText("Recommended Pump", 70, 235, text);

        text.setColor(Color.rgb(0, 83, 176));
        text.setTextSize(70);
        drawFit(c, safe(r.model), 70, 330, text, 940);

        int y = 395;
        drawChip(c, "HP", PumpSelector.trim(r.hp), 70, y, 210, 120);
        drawChip(c, "kW", PumpSelector.trim(r.kw), 300, y, 210, 120);
        drawChip(c, "Phase", phaseLabel(r.phase), 530, y, 230, 120);
        drawChip(c, "Type", compactType(r.category), 780, y, 230, 120);

        y = 550;
        box(c, 70, y, 1010, y + 70, Color.rgb(247, 251, 255), Color.rgb(207, 221, 236));
        text.setColor(Ui.TEXT);
        text.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        text.setTextSize(30);
        drawFit(c, has
                ? "Suitable for " + PumpSelector.head(head) + " head  •  Estimated " + PumpSelector.formatFlow(flow, unit)
                : "Model details and smooth performance curve",
                115, y + 45, text, 860);

        y = 650;
        box(c, 70, y, 1010, y + 90, Color.rgb(255, 248, 238), Color.rgb(255, 132, 0));
        text.setColor(Ui.TEXT);
        text.setTextSize(33);
        text.setTypeface(Typeface.create(Typeface.DEFAULT, Typeface.BOLD));
        c.drawText(has ? "Estimated at " + PumpSelector.head(head) + ":" : "Flow range:", 125, y + 58, text);

        text.setColor(Color.rgb(232, 92, 0));
        text.setTextSize(48);
